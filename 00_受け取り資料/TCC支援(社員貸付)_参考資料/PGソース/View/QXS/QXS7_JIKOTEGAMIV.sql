SELECT QWA1_PRMBUNSHOKANRINO,QWA1_BUNSHOMEI,QWA1_TOUZAIKBN,QWA1_BUNRUICD,QWA2_BUNRUIMEI,QWA1_KIJITSUKANRIKBN
