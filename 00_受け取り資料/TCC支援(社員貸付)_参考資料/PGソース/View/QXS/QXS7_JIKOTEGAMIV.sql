WHERE	QWA1_PRMBUNSHOKANRINO IN ('0758','0258','0259','0825','0826','0827',
	'0203','0207','0208','0210','0818','0819','0822','0221','0222','0223',
	'0293','0224','0296','0347','0239','0358','0757','0815','0817','0379')	AND
	QWA1_BUNRUICD = QWA2_PRMBUNRUICD;
