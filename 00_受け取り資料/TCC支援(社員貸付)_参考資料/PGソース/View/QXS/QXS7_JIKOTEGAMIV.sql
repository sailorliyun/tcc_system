CREATE OR REPLACE view QXS7_JIKOTEGAMIV(
	QXS7_BUNSHOKANRINO,
	QXS7_BUNSHOMEI,
	QXS7_TOUZAIKBN,
	QXS7_BUNRUICD,
	QXS7_BUNRUIMEI,
	QXS7_KIJITSUKANRIKBN)
