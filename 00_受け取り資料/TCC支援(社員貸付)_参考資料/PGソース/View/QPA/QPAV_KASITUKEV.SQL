CREATE OR REPLACE VIEW QPAV_KASITUKEV (
		 QPAV_TORIHIKIYMD				--取引日付
		,QPAV_TORIHIKIHMS				--取引日付時間
		,QPAV_SYAINCD					--社員コード
		,QPAV_SIMEIKANJI				--社員氏名
		,QPAV_KINGAKU					--貸付金額
		,QPAV_DATEYMD					--登録日付
		,QPAV_WSNAME					--(登録者ID)
		,QPAV_USERMEI					--(登録者名)
		,QPAV_MISECD					--店コード
		,QPAV_MISENM					--店名
		,QPAV_BUMONCD					--部門コード
		,QPAV_BUMONNM) AS				--部門名
	SELECT
		 QPA6_TORIHIKIYMD
		,QPA6_TORIHIKIHMS
		,QPA6_SYAINCD
		,QPAC_SIMEIKANJI
		,QPA6_KINGAKU
		,QPA6_DATEYMD
		,QPA6_WSNAME
		,QUA1_USERMEI
		,QPAC_MISECD
		,QPAE_MISENM
		,QPAC_BUMONCD
		,QPAF_BUMONNM
	FROM
		 QPA6_KGCKASITUKET
		,QPAC_SYAINM
		,QPAE_MISEM
		,QPAF_BUMONM
		,QUA2_USERIDV
	WHERE	QPA6_KGCKASITUKET.QPA6_HOSTSEQ = 'SY'
	AND		QPA6_KGCKASITUKET.QPA6_SYAINCD = QPAC_SYAINM.QPAC_PRMSYAINCD2
	AND		RTRIM(QPA6_KGCKASITUKET.QPA6_WSNAME)  = RTRIM(QUA2_USERIDV.QUA1_USERID)
	AND		QPAC_SYAINM.QPAC_MISECD  = QPAE_MISEM.QPAE_PRMMISECD(+)
	AND		QPAC_SYAINM.QPAC_MISECD  = QPAF_BUMONM.QPAF_PRMMISECD(+)
	AND		QPAC_SYAINM.QPAC_BUMONCD = QPAF_BUMONM.QPAF_PRMBUMONCD(+)
;
